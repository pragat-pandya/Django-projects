# A dictionary is a set of key-value pairs, where each key has a corresponding value, just like in a dictionary,
# each word (the key) has a corresponding definition (the value)
houses = {"Harry": "Gryffindor", "Draco": "Slytherin"}
# Print out Harry's house
print(houses["Harry"])
# Adding values to a dictionary:
houses["Hermione"] = "Gryffindor"
# Print out Hermione's House:
print(houses["Hermione"])

""" Output: Gryffindor Gryffindor """
